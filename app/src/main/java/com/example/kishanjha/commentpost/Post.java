package com.example.kishanjha.commentpost;

public class Post {
    String name,post,time,id;

    public Post() {
    }

    public Post(String name , String post , String time) {
        this.name = name;
        this.post = post;
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
